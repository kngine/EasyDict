/**
 * Get scenario object from key
 * @param {string} key - Scenario key (formal, neutral, informal, intimate)
 * @returns {Object} Scenario object
 */
function getScenarioFromKey(key) {
  const scenarios = {
    formal: UsageScenario.FORMAL,
    neutral: UsageScenario.NEUTRAL,
    informal: UsageScenario.INFORMAL,
    intimate: UsageScenario.INTIMATE
  };
  return scenarios[key] || UsageScenario.NEUTRAL;
}

/**
 * Analyze word usage and provide formality suggestions
 * @param {string} word - The word to analyze
 * @param {Array<string>} synonyms - Optional synonyms from dictionary
 * @returns {Object} Usage analysis with primary scenario and suggestions
 */
export function analyzeWordUsage(word, synonyms = []) {
  const lowercased = word.toLowerCase();
  const wordInfo = wordDatabase[lowercased];

  if (wordInfo) {
    const primaryScenario = getScenarioFromKey(wordInfo.primary);
    const suggestions = [];

    // Build suggestions for all scenarios
    const allScenarios = ['formal', 'neutral', 'informal', 'intimate'];
    for (const scenarioKey of allScenarios) {
      const scenario = getScenarioFromKey(scenarioKey);
      
      if (scenarioKey === wordInfo.primary) {
        suggestions.push({
          scenario,
          isAppropriate: true,
          suggestedWord: null,
          note: 'This word fits well in this context'
        });
      } else if (wordInfo.alternatives[scenarioKey]) {
        suggestions.push({
          scenario,
          isAppropriate: false,
          suggestedWord: wordInfo.alternatives[scenarioKey],
          note: null
        });
      }
    }

    return {
      primaryScenario,
      suggestions,
      hasContent: suggestions.length > 0
    };
  }

  // Word not in database - return general analysis
  const suggestions = [];
  const allScenarios = [UsageScenario.FORMAL, UsageScenario.NEUTRAL, UsageScenario.INFORMAL, UsageScenario.INTIMATE];
  
  for (const scenario of allScenarios) {
    suggestions.push({
      scenario,
      isAppropriate: true,
      suggestedWord: null,
      note: 'Generally appropriate'
    });
  }

  return {
    primaryScenario: UsageScenario.NEUTRAL,
    suggestions,
    hasContent: true
  };
}
