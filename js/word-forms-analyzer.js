// Build lookup dictionary for fast access
const wordLookup = new Map();

wordFamilies.forEach((family, index) => {
  const [verb, noun, adj, adv] = family;
  if (verb) wordLookup.set(verb.toLowerCase(), index);
  if (noun) wordLookup.set(noun.toLowerCase(), index);
  if (adj) wordLookup.set(adj.toLowerCase(), index);
  if (adv) wordLookup.set(adv.toLowerCase(), index);
});

/**
 * Analyze word forms for a given word
 * @param {string} word - The word to analyze
 * @returns {Object} Analysis result with forms array
 */
export function analyzeWordForms(word) {
  const lowercased = word.toLowerCase();
  const familyIndex = wordLookup.get(lowercased);

  if (familyIndex === undefined) {
    return { forms: [], hasContent: false };
  }

  const family = wordFamilies[familyIndex];
  const [verb, noun, adj, adv] = family;
  const forms = [];

  // Add forms in order: Verb, Noun, Adj, Adv (excluding the searched word)
  if (verb && verb.toLowerCase() !== lowercased) {
    forms.push({ word: verb, type: WordType.VERB });
  }
  if (noun && noun.toLowerCase() !== lowercased) {
    forms.push({ word: noun, type: WordType.NOUN });
  }
  if (adj && adj.toLowerCase() !== lowercased) {
    forms.push({ word: adj, type: WordType.ADJECTIVE });
  }
  if (adv && adv.toLowerCase() !== lowercased) {
    forms.push({ word: adv, type: WordType.ADVERB });
  }

  return {
    forms,
    hasContent: forms.length > 0
  };
}
